package bisdi.pertemuan02;

public class KelilingLuasPersegi{
        public static void main(String[] args){
            double panjang = 3.5;
            double lebar = 9.7;
            double keliling = 2 * (panjang + lebar);
            double luas = panjang * lebar;

            System.out.println("Keliling persegi dengan panjang "+panjang+" dan lebar "+lebar+" adalah "+keliling);
            System.out.println("Luas persegi dengan panjang "+panjang+" dan lebar "+lebar+" adalah "+luas);
        }
}



